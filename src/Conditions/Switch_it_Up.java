package Conditions;

public class Switch_it_Up {
    public static String switchItUp(int number) {
        String result = "";
        if (number == 1) {
            result = "One";
        }
        if (number == 2) {
            result = "Two";
        }
        if (number == 3) {
            result = "Three";
        }
        if (number == 4) {
            result = "Four";
        }
        if (number == 5) {
            result = "Five";
        }
        if (number == 6) {
            result = "Six";
        }
        if (number == 7) {
            result = "Seven";
        }
        if (number == 8) {
            result = "Eight";
        }
        if (number == 9) {
            result = "Nine";
        }
        if (number == 10) {
            result = "Ten";
        }
        return result;
    }
}
